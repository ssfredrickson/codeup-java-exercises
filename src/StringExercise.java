public class StringExercise {

    public static void main(String[] args) {
        String education = "We don't need no education \nWe dont need no thought control";
        System.out.println(education);

        String quotes = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quotes);

        String backslash = "In windows, the main drive is usually C:\\";
        System.out.println(backslash);

        String backslashes = " I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\\"";
        System.out.println(backslashes);
    }

}
